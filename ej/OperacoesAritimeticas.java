public class OperacoesAritimeticas {
  public static void main(String [] args) {
    
    int minhaIdade = 40;
    int suaIdade = 25;
    //Soma das idades 
    int somaIdades = minhaIdade + suaIdade;
    System.out.println("A soma das idades é de: " + somaIdades);
    //Subtração das idades 
    int subtracao = minhaIdade - suaIdade;
    System.out.println("A valor da subtracao é:" + subtracao);
    //Dobro idade 
    int dobroIdade = 2 * minhaIdade;
    System.out.println("O dobro da minha idade é de: " + dobroIdade);
    //Divisao
    int divisaoIdade = minhaIdade / 2;
    System.out.println("A metade de minha idade é de: " + divisaoIdade);
    //Modulo 
    int modulo = 7 % 2;
    System.out.println("Resultado do modulo: " + modulo);
  }
}
